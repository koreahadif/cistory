package nhn.cistory.vo;

public class Testtestresult {
	private Integer test_num = null;
	private String id = null;
	private Integer score = null;
	private String test_start_time = null;
	private String test_end_time = null;
	private Integer test_rslt_num = null;
	private Integer test_tmp_num = null;
	private Integer q_num = null;
	private Integer user_sol = null;
	public Testtestresult() {
		super();
	}
	public Testtestresult(Integer test_num, String id, Integer score,
			String test_start_time, String test_end_time,
			Integer test_rslt_num, Integer test_tmp_num, Integer q_num,
			Integer user_sol) {
		super();
		this.test_num = test_num;
		this.id = id;
		this.score = score;
		this.test_start_time = test_start_time;
		this.test_end_time = test_end_time;
		this.test_rslt_num = test_rslt_num;
		this.test_tmp_num = test_tmp_num;
		this.q_num = q_num;
		this.user_sol = user_sol;
	}
	public Integer getTest_num() {
		return test_num;
	}
	public void setTest_num(Integer test_num) {
		this.test_num = test_num;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Integer getScore() {
		return score;
	}
	public void setScore(Integer score) {
		this.score = score;
	}
	public String getTest_start_time() {
		return test_start_time;
	}
	public void setTest_start_time(String test_start_time) {
		this.test_start_time = test_start_time;
	}
	public String getTest_end_time() {
		return test_end_time;
	}
	public void setTest_end_time(String test_end_time) {
		this.test_end_time = test_end_time;
	}
	public Integer getTest_rslt_num() {
		return test_rslt_num;
	}
	public void setTest_rslt_num(Integer test_rslt_num) {
		this.test_rslt_num = test_rslt_num;
	}
	public Integer getTest_tmp_num() {
		return test_tmp_num;
	}
	public void setTest_tmp_num(Integer test_tmp_num) {
		this.test_tmp_num = test_tmp_num;
	}
	public Integer getQ_num() {
		return q_num;
	}
	public void setQ_num(Integer q_num) {
		this.q_num = q_num;
	}
	public Integer getUser_sol() {
		return user_sol;
	}
	public void setUser_sol(Integer user_sol) {
		this.user_sol = user_sol;
	}
	@Override
	public String toString() {
		return "Testtestresult [test_num=" + test_num + ", id=" + id
				+ ", score=" + score + ", test_start_time=" + test_start_time
				+ ", test_end_time=" + test_end_time + ", test_rslt_num="
				+ test_rslt_num + ", test_tmp_num=" + test_tmp_num + ", q_num="
				+ q_num + ", user_sol=" + user_sol + "]";
	}
	
	
}
